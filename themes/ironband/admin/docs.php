		<h2>Getting Started</h2>

		<ul>
			<li>We have included an import file that contains the exact copy of our demo.<br>
			You can import this file from the Import Default Data tab.<br>
			<strong>NB. This will erase all your existing posts, comments, links and metas before importing. Use wisely.</strong></li>
			<li>If you decide not to import the default theme data,<br>
			you need to create the different pages and assign them to the corresponding template.</li>
			<li>For the home / news page, make sure to set it as a static page within WordPress Settings / Reading</li>
			<li>Make sure to associate the pages under the Content Types tab.</li>

